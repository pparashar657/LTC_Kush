package introduction;

public class PrintStatement {

    public static void main(String[] args) {
        System.out.println("Hello world");
        System.out.print("I am khan");
        System.out.println("I live in India"); // string

        // vnajkdfnbv usdffnv fesionv dakn nadku nwown Dn ksnslk ml ms

        // This is a single line comment.

        /*
        This is
        a multi
        line
        comment.
         */

        System.out.println(12); // int example
        System.out.println(198.89); // double

        System.out.println(56.89f); // float
        System.out.println(57777.8);

        System.out.println('j'); // char [only single character]

        System.out.println(true); // boolean
        System.out.println("true"); // string

        System.out.println("Pawan " + "kumar");
        System.out.println("Pawan" + 123);

        System.out.println((12 + 34) * (5 - 19) / 3);

        System.out.println("Pawan " + 123 + " kumar");

        System.out.println("123" + 32 + "true");

//        System.out.println(true + true);

        System.out.println("Pawan" + (2 + 5));
        System.out.println("Pawan" + 2 + 5); // With string only '+' operator works.

        System.out.println(12 > 7);
    }

}
